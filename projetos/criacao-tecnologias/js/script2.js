const urlParams = new URLSearchParams(window.location.search);
const tecnologiaId = urlParams.get('id');

async function carregarDetalhesTecnologia() {
    try {
        const tecnologia = dados.find(item => item.id === parseInt(tecnologiaId));
        if (tecnologia) {
            exibirDetalhesTecnologia(tecnologia);
        } else {
            console.error('Tecnologia não encontrada.');
        }
    } catch (error) {
        console.error('Erro ao carregar os detalhes da tecnologia:', error);
    }
}

function exibirDetalhesTecnologia(tecnologia) {
    const tituloElement = document.getElementById('titulo-tecnologia');
    const imagemElement = document.getElementById('imagem-tecnologia');
    const descricaoElement = document.getElementById('descricao-tecnologia');
    const especificacoesElement = document.getElementById('especificacoes-tecnologia');
    const aplicacoesElement = document.getElementById('aplicacoes-tecnologia');

    tituloElement.textContent = tecnologia.titulo;
    imagemElement.src = tecnologia.imagem;
    imagemElement.alt = tecnologia.titulo;
    descricaoElement.textContent = tecnologia.descricao;

    especificacoesElement.innerHTML = '';
    tecnologia.especificacoes.forEach(especificacao => {
        const li = document.createElement('li');
        li.textContent = especificacao;
        especificacoesElement.appendChild(li);
    });

    aplicacoesElement.innerHTML = '';
    tecnologia.aplicacoes.forEach(aplicacao => {
        const li = document.createElement('li');
        li.textContent = aplicacao;
        aplicacoesElement.appendChild(li);
    });
}


document.addEventListener('DOMContentLoaded', carregarDetalhesTecnologia);
